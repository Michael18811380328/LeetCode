import { addBinary } from '../finished/67-addTwoBinary';

test('67-reverse-number', () => {
  expect(addBinary('1010', '1011')).toEqual('10101');
  expect(addBinary('11', '1')).toEqual('100');
  expect(addBinary('0', '1')).toEqual('1');
});

test('67-reverse-number-long', () => {
  const a = '10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101';
  const b = '110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011';
  const result = '110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000';
  expect(addBinary(a, b)).toEqual(result);
});
